package com.tutorial;

public class Main {
    public static void main(String[] args) {
        // Operator Assignments

        // Assignments jumlah
        int a = 1;
        a += 10; // a = a + 10;
        System.out.println("nilai a = " + a);

        // Assignments pengurangan
        int b = 100;
        b -= 10; // a = a + 10;
        System.out.println("nilai b = " + b);

        // Assignments perkalian
        int c = 100;
        c *= 10; // a = a + 10;
        System.out.println("nilai c = " + c);

        // Assignments pembagian
        int d = 100;
        d /= 10; // a = a + 10;
        System.out.println("nilai d = " + d);

        // Assignments modulus
        int e = 10;
        e %= 17; // a = a + 10;
        System.out.println("nilai e = " + e);
    }
}
